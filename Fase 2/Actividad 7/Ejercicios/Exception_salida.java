public class Exception_salida extends Exception {

    public Exception_salida() {
    }

    public Exception_salida(String message) {
        super(message);
    }

    public Exception_salida(Throwable cause) {
        super(cause);
    }

    public Exception_salida(String message, Throwable cause) {
        super(message, cause);
    }

    public Exception_salida(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
    
}
